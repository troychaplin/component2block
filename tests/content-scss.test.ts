import { describe, it, expect } from 'vitest';
import { generateContentScss } from '../src/generators/content-scss.js';
import type { StbConfig } from '../src/types.js';

const config: StbConfig = {
  prefix: 'design-system',
  tokensPath: 'src/styles/tokens.css',
  outDir: 'dist/wp',
  wpThemeable: false,
  tokens: {
    fontFamily: {
      inter: { value: 'Inter, sans-serif', name: 'Inter', slug: 'inter' },
    },
    fontSize: {
      small: { value: '1rem', name: 'Small', slug: 'small' },
      medium: { value: '1.125rem', name: 'Medium', slug: 'medium' },
    },
  },
  baseStyles: {
    body: {
      fontFamily: 'inter',
      fontSize: 'medium',
      fontWeight: '400',
      lineHeight: '1.6',
    },
    heading: {
      fontFamily: 'inter',
    },
    h1: { fontSize: '4.5rem', fontWeight: '500' },
    h2: { fontSize: '3rem', fontWeight: '500' },
    h6: { fontSize: '1.45rem', fontWeight: '500', fontStyle: 'italic' },
    caption: { fontSize: 'small', fontStyle: 'italic', fontWeight: '300' },
  },
};

describe('generateContentScss', () => {
  it('returns null when no baseStyles', () => {
    const result = generateContentScss({ ...config, baseStyles: undefined });
    expect(result).toBeNull();
  });

  it('includes auto-generated header', () => {
    const result = generateContentScss(config)!;
    expect(result).toContain('Auto-generated by component2block');
  });

  it('body declarations are wrapped in body selector', () => {
    const result = generateContentScss(config)!;
    expect(result).toContain('body {');
    const bodyBlock = result.split('body {')[1].split('}')[0];
    expect(bodyBlock).toContain('font-family:');
  });

  it('body resolves token references to CSS variables', () => {
    const result = generateContentScss(config)!;
    expect(result).toContain('  font-family: var(--design-system--font-family-inter);');
    expect(result).toContain('  font-size: var(--design-system--font-size-medium);');
  });

  it('body passes raw values through unchanged', () => {
    const result = generateContentScss(config)!;
    expect(result).toContain('  font-weight: 400;');
    expect(result).toContain('  line-height: 1.6;');
  });

  it('heading group uses :where(h1, h2, h3, h4, h5, h6) selector', () => {
    const result = generateContentScss(config)!;
    expect(result).toContain(':where(h1, h2, h3, h4, h5, h6) {');
    expect(result).toContain('  font-family: var(--design-system--font-family-inter);');
  });

  it('individual headings use :where(hN) selectors', () => {
    const result = generateContentScss(config)!;
    expect(result).toContain(':where(h1) {');
    expect(result).toContain(':where(h2) {');
  });

  it('individual headings include font-style: normal by default', () => {
    const result = generateContentScss(config)!;
    // h1 has no fontStyle in config — should get normal
    const h1Block = result.split(':where(h1) {')[1].split('}')[0];
    expect(h1Block).toContain('font-style: normal;');
  });

  it('explicit fontStyle overrides the normal default', () => {
    const result = generateContentScss(config)!;
    // h6 has fontStyle: 'italic' in config
    const h6Block = result.split(':where(h6) {')[1].split('}')[0];
    expect(h6Block).toContain('font-style: italic;');
    expect(h6Block).not.toContain('font-style: normal;');
  });

  it('caption maps to :where(figcaption) selector', () => {
    const result = generateContentScss(config)!;
    expect(result).toContain(':where(figcaption) {');
  });

  it('caption resolves token references', () => {
    const result = generateContentScss(config)!;
    const captionBlock = result.split(':where(figcaption) {')[1].split('}')[0];
    expect(captionBlock).toContain('font-size: var(--design-system--font-size-small);');
  });

  it('outputs properties in consistent order', () => {
    const result = generateContentScss(config)!;
    // In body: fontFamily before fontSize before fontWeight before lineHeight
    const fontFamilyIdx = result.indexOf('  font-family: var(--design-system--font-family-inter);');
    const fontSizeIdx = result.indexOf('  font-size: var(--design-system--font-size-medium);');
    const fontWeightIdx = result.indexOf('  font-weight: 400;');
    const lineHeightIdx = result.indexOf('  line-height: 1.6;');
    expect(fontFamilyIdx).toBeLessThan(fontSizeIdx);
    expect(fontSizeIdx).toBeLessThan(fontWeightIdx);
    expect(fontWeightIdx).toBeLessThan(lineHeightIdx);
  });
});

describe('generateContentScss — spacing padding', () => {
  const spacingConfig: StbConfig = {
    prefix: 'design-system',
    tokensPath: 'src/styles/tokens.css',
    outDir: 'dist/wp',
    wpThemeable: false,
    tokens: {
      spacing: {
        large: { value: 'min(2.25rem, 3vw)', slug: '60', name: 'Large' },
      },
      fontFamily: {
        inter: { value: 'Inter, sans-serif', name: 'Inter', slug: 'inter' },
      },
    },
    baseStyles: {
      body: { fontFamily: 'inter', fontWeight: '400' },
      spacing: {
        padding: {
          top: '0',
          right: 'large',
          bottom: '0',
          left: 'large',
        },
      },
    },
  };

  it('generates root padding CSS variables in body block', () => {
    const result = generateContentScss(spacingConfig)!;
    expect(result).toContain('  --design-system--root-padding-top: 0;');
    expect(result).toContain('  --design-system--root-padding-right: var(--design-system--spacing-large);');
    expect(result).toContain('  --design-system--root-padding-bottom: 0;');
    expect(result).toContain('  --design-system--root-padding-left: var(--design-system--spacing-large);');
  });

  it('generates .has-global-padding class with horizontal padding', () => {
    const result = generateContentScss(spacingConfig)!;
    expect(result).toContain('.has-global-padding {');
    expect(result).toContain('  padding-right: var(--design-system--root-padding-right);');
    expect(result).toContain('  padding-left: var(--design-system--root-padding-left);');
  });

  it('generates .alignfull breakout rules', () => {
    const result = generateContentScss(spacingConfig)!;
    expect(result).toContain('.has-global-padding > .alignfull {');
    expect(result).toContain('  max-width: none;');
    expect(result).toContain('  margin-right: calc(var(--design-system--root-padding-right) * -1);');
    expect(result).toContain('  margin-left: calc(var(--design-system--root-padding-left) * -1);');
  });

  it('generates nested .has-global-padding restore rule', () => {
    const result = generateContentScss(spacingConfig)!;
    expect(result).toContain('.has-global-padding > .alignfull > .has-global-padding {');
    expect(result).toContain('  padding-right: var(--design-system--root-padding-right);');
    expect(result).toContain('  padding-left: var(--design-system--root-padding-left);');
  });

  it('resolves token references in padding values', () => {
    const result = generateContentScss(spacingConfig)!;
    expect(result).toContain('var(--design-system--spacing-large)');
  });

  it('passes raw padding values through unchanged', () => {
    const result = generateContentScss(spacingConfig)!;
    expect(result).toContain('--design-system--root-padding-top: 0;');
    expect(result).toContain('--design-system--root-padding-bottom: 0;');
  });

  it('creates body block with just padding vars when no body typography', () => {
    const spacingOnlyConfig: StbConfig = {
      ...spacingConfig,
      baseStyles: {
        spacing: {
          padding: { right: 'large', left: 'large' },
        },
      },
    };
    const result = generateContentScss(spacingOnlyConfig)!;
    expect(result).toContain('body {');
    expect(result).toContain('--design-system--root-padding-right: var(--design-system--spacing-large);');
    expect(result).not.toContain('font-family:');
  });

  it('produces no padding or alignfull rules when no spacing config', () => {
    const noSpacingConfig: StbConfig = {
      ...spacingConfig,
      baseStyles: {
        body: { fontFamily: 'inter' },
      },
    };
    const result = generateContentScss(noSpacingConfig)!;
    expect(result).not.toContain('root-padding');
    expect(result).not.toContain('.has-global-padding');
    expect(result).not.toContain('.alignfull');
  });

  it('only outputs defined padding sides', () => {
    const partialConfig: StbConfig = {
      ...spacingConfig,
      baseStyles: {
        spacing: {
          padding: { right: 'large', left: 'large' },
        },
      },
    };
    const result = generateContentScss(partialConfig)!;
    expect(result).toContain('--design-system--root-padding-right:');
    expect(result).toContain('--design-system--root-padding-left:');
    expect(result).not.toContain('--design-system--root-padding-top:');
    expect(result).not.toContain('--design-system--root-padding-bottom:');
  });
});

describe('generateContentScss — spacing blockGap', () => {
  const blockGapConfig: StbConfig = {
    prefix: 'design-system',
    tokensPath: 'src/styles/tokens.css',
    outDir: 'dist/wp',
    wpThemeable: false,
    tokens: {
      spacing: {
        medium: { value: 'min(1.5rem, 2vw)', slug: '50', name: 'Medium' },
        large: { value: 'min(2.25rem, 3vw)', slug: '60', name: 'Large' },
      },
    },
    baseStyles: {
      spacing: {
        blockGap: 'medium',
      },
    },
  };

  it('generates root block gap CSS variable in body block', () => {
    const result = generateContentScss(blockGapConfig)!;
    expect(result).toContain('  --design-system--root-block-gap: var(--design-system--spacing-medium);');
  });

  it('generates constrained layout rule', () => {
    const result = generateContentScss(blockGapConfig)!;
    expect(result).toContain(':where(.is-layout-constrained) > * + * {');
    expect(result).toContain('  margin-block-start: var(--design-system--root-block-gap);');
  });

  it('generates flex layout rule', () => {
    const result = generateContentScss(blockGapConfig)!;
    expect(result).toContain(':where(.is-layout-flex) {');
    expect(result).toContain('  gap: var(--design-system--root-block-gap);');
  });

  it('generates grid layout rule', () => {
    const result = generateContentScss(blockGapConfig)!;
    expect(result).toContain(':where(.is-layout-grid) {');
    expect(result).toContain('  gap: var(--design-system--root-block-gap);');
  });

  it('resolves token references in blockGap value', () => {
    const result = generateContentScss(blockGapConfig)!;
    expect(result).toContain('var(--design-system--spacing-medium)');
  });

  it('passes raw blockGap values through unchanged', () => {
    const rawConfig: StbConfig = {
      ...blockGapConfig,
      baseStyles: {
        spacing: { blockGap: '2rem' },
      },
    };
    const result = generateContentScss(rawConfig)!;
    expect(result).toContain('--design-system--root-block-gap: 2rem;');
  });

  it('does not produce block gap rules when blockGap absent', () => {
    const noGapConfig: StbConfig = {
      ...blockGapConfig,
      baseStyles: {
        spacing: {
          padding: { right: 'large', left: 'large' },
        },
      },
    };
    const result = generateContentScss(noGapConfig)!;
    expect(result).not.toContain('root-block-gap');
    expect(result).not.toContain('.is-layout-constrained');
    expect(result).not.toContain('.is-layout-flex');
    expect(result).not.toContain('.is-layout-grid');
  });

  it('generates both blockGap and padding when both defined', () => {
    const bothConfig: StbConfig = {
      ...blockGapConfig,
      baseStyles: {
        spacing: {
          blockGap: 'medium',
          padding: { right: 'large', left: 'large' },
        },
      },
    };
    const result = generateContentScss(bothConfig)!;
    expect(result).toContain('--design-system--root-block-gap:');
    expect(result).toContain('--design-system--root-padding-right:');
    expect(result).toContain('.is-layout-constrained');
    expect(result).toContain('.has-global-padding');
  });

  it('creates body block with just blockGap when no typography or padding', () => {
    const result = generateContentScss(blockGapConfig)!;
    expect(result).toContain('body {');
    expect(result).toContain('--design-system--root-block-gap:');
    expect(result).not.toContain('font-family:');
    expect(result).not.toContain('root-padding');
  });
});

describe('generateContentScss — baseStyles color', () => {
  const colorConfig: StbConfig = {
    prefix: 'design-system',
    tokensPath: 'src/styles/tokens.css',
    outDir: 'dist/wp',
    wpThemeable: false,
    tokens: {
      colorPalette: {
        primary: { value: '#0073aa', name: 'Primary', slug: 'primary' },
        secondary: { value: '#23282d', name: 'Secondary', slug: 'secondary' },
        base: { value: '#ffffff', name: 'Base', slug: 'base' },
      },
      fontFamily: {
        inter: { value: 'Inter, sans-serif', name: 'Inter', slug: 'inter' },
      },
    },
  };

  it('emits color declaration in body block', () => {
    const cfg: StbConfig = {
      ...colorConfig,
      baseStyles: {
        body: { color: 'secondary' },
      },
    };
    const result = generateContentScss(cfg)!;
    expect(result).toContain('color: var(--design-system--color-secondary);');
  });

  it('emits background-color declaration in body block', () => {
    const cfg: StbConfig = {
      ...colorConfig,
      baseStyles: {
        body: { background: 'base' },
      },
    };
    const result = generateContentScss(cfg)!;
    expect(result).toContain('background-color: var(--design-system--color-base);');
  });

  it('emits both color and background-color alongside typography', () => {
    const cfg: StbConfig = {
      ...colorConfig,
      baseStyles: {
        body: { fontFamily: 'inter', color: 'secondary', background: 'base' },
      },
    };
    const result = generateContentScss(cfg)!;
    expect(result).toContain('font-family: var(--design-system--font-family-inter);');
    expect(result).toContain('color: var(--design-system--color-secondary);');
    expect(result).toContain('background-color: var(--design-system--color-base);');
  });

  it('emits heading color', () => {
    const cfg: StbConfig = {
      ...colorConfig,
      baseStyles: {
        heading: { color: 'primary' },
      },
    };
    const result = generateContentScss(cfg)!;
    expect(result).toContain(':where(h1, h2, h3, h4, h5, h6) {');
    expect(result).toContain('color: var(--design-system--color-primary);');
  });

  it('emits individual heading color', () => {
    const cfg: StbConfig = {
      ...colorConfig,
      baseStyles: {
        h1: { fontSize: '4.5rem', color: 'primary' },
      },
    };
    const result = generateContentScss(cfg)!;
    expect(result).toContain(':where(h1) {');
    expect(result).toContain('color: var(--design-system--color-primary);');
  });

  it('passes raw color values through', () => {
    const cfg: StbConfig = {
      ...colorConfig,
      baseStyles: {
        body: { color: '#333333', background: '#ffffff' },
      },
    };
    const result = generateContentScss(cfg)!;
    expect(result).toContain('color: #333333;');
    expect(result).toContain('background-color: #ffffff;');
  });

  it('does not emit color declarations when not defined', () => {
    const cfg: StbConfig = {
      ...colorConfig,
      baseStyles: {
        body: { fontFamily: 'inter' },
      },
    };
    const result = generateContentScss(cfg)!;
    expect(result).not.toMatch(/^\s+color:/m);
    expect(result).not.toContain('background-color:');
  });

  it('emits caption color', () => {
    const cfg: StbConfig = {
      ...colorConfig,
      baseStyles: {
        caption: { color: 'secondary' },
      },
    };
    const result = generateContentScss(cfg)!;
    expect(result).toContain(':where(figcaption) {');
    expect(result).toContain('color: var(--design-system--color-secondary);');
  });
});

