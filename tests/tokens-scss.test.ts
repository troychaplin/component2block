import { describe, it, expect } from 'vitest';
import { generateTokensScss } from '../src/generators/tokens-scss.js';
import type { C2bConfig } from '../src/types.js';

const config: C2bConfig = {
  prefix: 'test',
  srcDir: 'src/styles',
  themeDir: 'dist/wp',
  bundleFonts: false,
  tokens: {
    colorPalette: {
      primary: { value: '#0073aa', name: 'Primary', slug: 'primary' },
      'primary-hover': { value: '#005a87' },
      muted: { value: '#999999', cssOnly: true },
    },
    spacing: {
      md: { value: '1rem', slug: '40', name: 'Medium' },
    },
    fontFamily: {
      base: { value: 'sans-serif', name: 'Sans', slug: 'body' },
    },
    fontSize: {
      sm: { value: '0.875rem', slug: 'small', name: 'Small' },
      xs: { value: '0.75rem' },
    },
    zIndex: {
      modal: { value: '300' },
    },
  },
};

describe('generateTokensScss', () => {
  const output = generateTokensScss(config);

  it('includes the auto-generated header', () => {
    expect(output).toContain('// Auto-generated by component2block');
  });

  it('generates correct color variables', () => {
    expect(output).toContain('$test-color-primary: #0073aa;');
    expect(output).toContain('$test-color-primary-hover: #005a87;');
  });

  it('includes cssOnly tokens (SCSS is another compile-time output)', () => {
    expect(output).toContain('$test-color-muted: #999999;');
  });

  it('generates correct spacing variables', () => {
    expect(output).toContain('$test-spacing-md: 1rem;');
  });

  it('generates correct font-family variables', () => {
    expect(output).toContain('$test-font-family-base: sans-serif;');
  });

  it('generates correct font-size variables', () => {
    expect(output).toContain('$test-font-size-sm: 0.875rem;');
    expect(output).toContain('$test-font-size-xs: 0.75rem;');
  });

  it('generates correct z-index variables', () => {
    expect(output).toContain('$test-z-modal: 300;');
  });

  it('groups tokens by category with line comments', () => {
    expect(output).toContain('// Colors');
    expect(output).toContain('// Spacing');
    expect(output).toContain('// Z-Index');
  });

  it('does not wrap in :root or any selector', () => {
    expect(output).not.toContain(':root');
    expect(output).not.toContain('{');
    expect(output).not.toContain('}');
  });

  it('uses single dash separators (not CSS custom property --)', () => {
    expect(output).not.toContain('--');
    expect(output).not.toMatch(/^\$[\w-]+--/m);
  });

  it('uses hardcoded values for all tokens (no var() wrappers)', () => {
    expect(output).not.toContain('var(');
  });
});

describe('generateTokensScss — layout tokens', () => {
  const layoutConfig: C2bConfig = {
    prefix: 'test',
    srcDir: 'src/styles',
    themeDir: 'dist/wp',
    bundleFonts: false,
    tokens: {
      layout: {
        contentSize: { value: '645px' },
        wideSize: { value: '1340px' },
      },
    },
  };

  const output = generateTokensScss(layoutConfig);

  it('applies camelToKebab conversion for layout keys', () => {
    expect(output).toContain('$test-layout-content-size: 645px;');
    expect(output).toContain('$test-layout-wide-size: 1340px;');
  });

  it('groups layout tokens with a comment', () => {
    expect(output).toContain('// Layout');
  });
});

describe('generateTokensScss — fluid font sizes', () => {
  const fluidConfig: C2bConfig = {
    prefix: 'test',
    srcDir: 'src/styles',
    themeDir: 'dist/wp',
    bundleFonts: false,
    tokens: {
      fontSize: {
        medium: {
          value: '1.125rem',
          slug: 'medium',
          name: 'Medium',
          fluid: { min: '0.875rem', max: '1.125rem' },
        },
        static: {
          value: '0.75rem',
          slug: 'static',
          name: 'Static',
        },
      },
    },
  };

  const output = generateTokensScss(fluidConfig);

  it('generates clamp() for fluid font sizes identical to tokens.css', () => {
    expect(output).toContain(
      '$test-font-size-medium: clamp(0.875rem, 0.875rem + ((1vw - 0.2rem) * 0.313), 1.125rem);'
    );
  });

  it('uses static value when no fluid is defined', () => {
    expect(output).toContain('$test-font-size-static: 0.75rem;');
  });
});

describe('generateTokensScss — snapshot', () => {
  it('matches the expected file shape', () => {
    const snapshotConfig: C2bConfig = {
      prefix: 'rds',
      srcDir: 'src/styles',
      themeDir: 'dist/wp',
      bundleFonts: false,
      tokens: {
        colorPalette: {
          primary: { value: '#e91c24' },
          secondary: { value: '#426587' },
        },
        layout: {
          contentSize: { value: '1024px' },
          wideSize: { value: '1280px' },
        },
      },
    };
    expect(generateTokensScss(snapshotConfig)).toBe(
      [
        '// Auto-generated by component2block — do not edit manually',
        '',
        '// Colors',
        '$rds-color-primary: #e91c24;',
        '$rds-color-secondary: #426587;',
        '',
        '// Layout',
        '$rds-layout-content-size: 1024px;',
        '$rds-layout-wide-size: 1280px;',
        '',
      ].join('\n'),
    );
  });
});
